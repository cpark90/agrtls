"""
설정 모듈.
앵커 좌표와 모든 튜닝 파라미터를 한곳에 모았습니다.
앵커를 늘리거나 줄이려면 ANCHORS 딕셔너리만 수정하면 됩니다 —
최소 앵커 수, 색상 배정, 맵 축 범위, 측위 입력 수집이 모두 자동으로 따라갑니다.
"""

# ==================== 네트워크 ====================
LISTEN_IP = "0.0.0.0"      # 모든 인터페이스에서 수신
LISTEN_PORT = 5005         # ESP32의 SERVER_PORT와 동일하게

# ==================== 표시 ====================
WINDOW_TIME_SEC = 10       # 이동 창(Moving Window) 시간 범위 (초)

# ==================== 칼만 필터 튜닝 ====================
# Range 채널 (거리: 타이밍 지터라 비교적 작은 노이즈)
RANGE_Q = 0.01             # 프로세스 잡음: 클수록 측정값을 빨리 따라감
RANGE_R = 0.1              # 측정 잡음: 클수록 측정값을 덜 믿음(부드러움↑)

# RX Power 채널 (수신 전력: 변동 폭이 커서 R을 크게)
RX_Q = 0.05
RX_R = 4.0

# 위치 좌표 평활화 (x·y·z 각 축 독립 1D 칼만)
POS_Q = 0.05
POS_R = 0.2

# ==================== 아웃라이어 리젝션 ====================
GATE_SIGMA = 3.0           # 예측 분포에서 이 시그마 배수 이상 벗어나면 아웃라이어
MAX_REJECT_STREAK = 5      # 연속 거부 한계 초과 시 강제 수용(재수렴)

# ==================== 측위 ====================
RANGE_TIMEOUT_SEC = 1.0    # 이 시간 이상 갱신 안 된 앵커 거리는 측위에서 제외
SYNC_WINDOW_SEC = 0.3      # 측위 기준 시각에서 ±이 시간 안의 거리값만 동기화된 것으로 사용
GN_MAX_ITER = 10           # 가우스-뉴턴 최대 반복
GN_TOL = 1e-4              # 수렴 판정 (위치 변화량, m)
MAP_MARGIN_M = 1.5         # 맵 축 범위 여유 (m)

# ===== 앵커 좌표 (단위: m) — 여기에 추가/삭제만 하면 자동 확장 =====
# key: RANGE 메시지의 parts[1] (short address, 대문자 HEX)
# value: (x, y, z) — 3D 좌표로 적되, 2D 측위 시에는 z가 자동으로 무시됩니다.
ANCHORS = {
    "A000": (0.0, 0.0, 0.0),
    "A001": (1.7, 0.0, 0.0),
    "A002": (0.0, 1.7, 0.0),
    "A003": (5.0, 5.0, 0.0),
    "A004": (2.5, 2.5, 3.0),
    # 앵커를 더 추가하려면 여기에 한 줄씩:
    # "1A06": (1.0, 3.0, 2.0),
}


def anchors_for_dim(dim):
    """차원(dim=2 또는 3)에 맞춰 앵커 좌표를 잘라 반환.
    2D면 (x, y)만, 3D면 (x, y, z) 그대로."""
    out = {}
    for k, v in ANCHORS.items():
        out[k] = tuple(v[:dim])
    return out


def min_anchors_for_dim(dim):
    """차원별 측위 최소 앵커 수. 2D는 3개, 3D는 4개 (이론적 최소).
    등록된 앵커가 그보다 적으면 등록 수로 낮춤."""
    need = dim + 1   # 2D→3, 3D→4
    return min(need, len(ANCHORS))


# 하위 호환용 (3D 기준)
MIN_ANCHORS_FOR_FIX = min_anchors_for_dim(3)
